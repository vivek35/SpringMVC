package com.first.firstbeans;

public class LoginBean {
	String txtUsername;
	String txtPassword;
	
	String txtUsernameError;
	String txtPasswordError;
	String txtUserPassError;
	
	public String getTxtUserPassError() {
		return txtUserPassError;
	}
	public void setTxtUserPassError(String txtUserPassError) {
		this.txtUserPassError = txtUserPassError;
	}
	public String getTxtUsername() {
		return txtUsername;
	}
	public void setTxtUsername(String txtUsername) {
		this.txtUsername = txtUsername;
	}
	public String getTxtPassword() {
		return txtPassword;
	}
	public void setTxtPassword(String txtPassword) {
		this.txtPassword = txtPassword;
	}
	public String getTxtUsernameError() {
		return txtUsernameError;
	}
	public void setTxtUsernameError(String txtUsernameError) {
		this.txtUsernameError = txtUsernameError;
	}
	public String getTxtPasswordError() {
		return txtPasswordError;
	}
	public void setTxtPasswordError(String txtPasswordError) {
		this.txtPasswordError = txtPasswordError;
	}
}
